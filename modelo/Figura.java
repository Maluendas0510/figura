package modelo;

public class Figura 
{
    // atributos
    protected double perimetro;
    protected double area;
    


    //metodos
    public Figura(double perimetro , double area )
    {
        this.perimetro=0;
        this.area=0;
    }
    public double getPerimetro()
    {
        return perimetro;
    }
    public double getArea() 
    {
        return area;
    }
    
        
    
    
    


    
}
